public class mst003901_YotoKbDictionary {
	/**
	 * ＤＢ内のコード
	 */
	private String code = null;
	/**
	 * ＤＢ内コードの意味
	 */
	private String name = null;
	/**
	 * 対応したクラスを保持するマップ
	 */
	private static final Map map = new HashMap();

	/**
	 * コンストラクタ
	 * @param code ＤＢ内のコード
	 * @param name ＤＢ内コードの意味
	 */
	private mst003901_YotoKbDictionary(String code, String name) {
		this.code = code;
		this.name = name;
		map.put(code,this);
	}

	/**
	 * ＤＢ内コードの意味を返す
	 * @return String
	 */
	public String toString()
	{
		return name;
	}

	/**
	 * ＤＢ内のコードを返す
	 * @return String
	 */
	public String getCode()
	{
		return code;
	}

	/**
	 * コードで検索を行う
	 * @param key ＤＢ内のコード
	 * @return TaisyoKbDictionary
	 */
	public static mst003901_YotoKbDictionary getStatus(String key)
	{
		mst003901_YotoKbDictionary ret = (mst003901_YotoKbDictionary)map.get(key);
		if( ret == null )
			ret = UNKNOWN;
		return ret;
	}

	/**
	 * コードで検索を行う
	 * @param key ＤＢ内のコード
	 * @return TaisyoKbDictionary
	 */
	public static mst003901_YotoKbDictionary getStatus(long key)
	{
		return getStatus(Long.toString(key));
	}

	/**
	 * コードで検索を行う
	 * @param key ＤＢ内のコード
	 * @return TaisyoKbDictionary
	 */
	public static mst003901_YotoKbDictionary getStatus(int key)
	{
		return getStatus(Integer.toString(key));
	}

	/**
	 * 同じかどうかを比較する
	 * @param obj 比較の対象オブジェクト
	 * @return おなじかどうか
	 */
	public boolean equals( Object obj )
	{
		if( !(obj instanceof mst003901_YotoKbDictionary) )
			return false;
		String objStr = ((mst003901_YotoKbDictionary)obj).toString();
		String thisStr = toString();
		return objStr.equals( thisStr );
	}

	public static mst003901_YotoKbDictionary HACHU = new mst003901_YotoKbDictionary("1", "発注用");
	public static mst003901_YotoKbDictionary BUTURYU = new mst003901_YotoKbDictionary("2", "物流用");
	public static mst003901_YotoKbDictionary JYOUHOU = new mst003901_YotoKbDictionary("3", "情報用");
	public static mst003901_YotoKbDictionary HANSOKU = new mst003901_YotoKbDictionary("4", "販促用");
	public static mst003901_YotoKbDictionary UNKNOWN = new mst003901_YotoKbDictionary("X","UNKNOWN");
}
